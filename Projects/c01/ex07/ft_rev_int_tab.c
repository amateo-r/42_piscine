void	ft_rev_int_tab (int *tab, int size)
{
	int	i;
	int	n;

	i = 0;
	size--;
	while (size > i)
	{
		n = tab[i];
		tab[i] = tab[size];
		tab[size] = n;
		size--;
		i++;
	}
}
